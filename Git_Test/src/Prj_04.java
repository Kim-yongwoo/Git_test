
public class Prj_04 {

	public static void main(String[] args) {
		int a, b = 5;
		int c = 3;
		
		a = b + c;
		System.out.printf(" %d + %d = %d \n", b, c, a);
		
		a = b - c;
		System.out.printf(" %d - %d = %d \n", b, c, a);

		a = b * c;
		System.out.printf(" %d * %d = %d \n", b, c, a);
		
		a = b / c;
		System.out.printf(" %d / %d = %d \n", b, c, a);
		
		a = b % c;
		System.out.printf(" %d %% %d = %d \n", b, c, a);
	}

}
